#!/bin/bash
# Скрипт автоматической синхронизации CS 1.6 файлов на GitHub

# 1. Переходим в папку проекта
cd "$(dirname "$0")" || exit

# 2. Добавляем все изменения (кроме .gitignore-игнорируемых файлов)
git add .

# 3. Делаем коммит с текущей датой и временем
git commit -m "Автосинхронизация: $(date '+%Y-%m-%d %H:%M:%S')" || echo "Нет изменений для коммита"

# 4. Подтягиваем обновления с GitHub (чтобы не было конфликтов)
git pull origin main --rebase

# 5. Отправляем изменения на GitHub
git push origin main

echo "✅ Синхронизация завершена!"
